public class unary_2 {
    public static void main(String args[]){
        int a= 10;
        int b =10;
        System.out.println(a);
        System.out.println(a++); //First Value use then value change
        System.out.println(a);

        System.out.println(b);
        System.out.println(++b);  //First Value change then value use
        System.out.println(b);
    }
}
